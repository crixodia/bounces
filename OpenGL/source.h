class Source {
public:
	Point position;
	Shader shader;
	float* vertices;
	float* normals;
	float scale;
	int size;

	Source(Point position, Shader shader, float* vertices, float* normals, float scale, int size) {
		this->position = position;
		this->shader = shader;
		this->vertices = vertices;
		this->normals = normals;
		this->scale = scale;
		this->size = size;
	}

	Source(Point position, Shader shader, float* normals, float scale, int size) {
		this->position = position;
		this->shader = shader;
		this->vertices = icosahedron;
		this->normals = normals;
		this->scale = scale;
		this->size = size;
	}

	void transform(glm::mat4 model, glm::mat4 view, glm::mat4 projection) {
		shader.use();
		shader.setVec4("color", glm::vec4(246.0f / 255.0f, 48.0f / 255.0f, 0.0f, 0.0f));

		glm::mat4 sourceTransform = glm::mat4(1.0f);
		sourceTransform = glm::translate(sourceTransform, glm::vec3(position.x, position.y, position.z));
		sourceTransform = glm::scale(sourceTransform, glm::vec3(scale));

		shader.setMat4("model", sourceTransform);
		shader.setMat4("view", view);
		shader.setMat4("projection", projection);

	}
};

